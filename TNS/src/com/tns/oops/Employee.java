package com.tns.oops;


	//Encapsulation Example  
	public class Employee {
			private int eid;
			private int salary;
			public int getEid() {
				return eid;
			}
			public void setEid(int eid) {
				this.eid = eid;
			}
			public int getSalary() {
				return salary;
			}
			public void setSalary(int salary) {
				this.salary = salary;
			}
			
	}

